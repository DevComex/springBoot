package cn.gyyx.action.beans.mobile.website;

import java.util.Date;

public class MobileServiceBean {
	private int code;
	private int serverCode;
	private String serverName;
	private int nettypeCode;
	private Date obtainDate;
	private int mobileRegisteCode;
	private int isDeleted;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getServerCode() {
		return serverCode;
	}
	public void setServerCode(int serverCode) {
		this.serverCode = serverCode;
	}
	public String getServerName() {
		return serverName;
	}
	public void setServerName(String serverName) {
		this.serverName = serverName;
	}
	public int getNettypeCode() {
		return nettypeCode;
	}
	public void setNettypeCode(int nettypeCode) {
		this.nettypeCode = nettypeCode;
	}
	public Date getObtainDate() {
		return obtainDate;
	}
	public void setObtainDate(Date obtainDate) {
		this.obtainDate = obtainDate;
	}
	public int getMobileRegisteCode() {
		return mobileRegisteCode;
	}
	public void setMobileRegisteCode(int mobileRegisteCode) {
		this.mobileRegisteCode = mobileRegisteCode;
	}
	public int getIsDeleted() {
		return isDeleted;
	}
	public void setIsDeleted(int isDeleted) {
		this.isDeleted = isDeleted;
	}
	
	
}
