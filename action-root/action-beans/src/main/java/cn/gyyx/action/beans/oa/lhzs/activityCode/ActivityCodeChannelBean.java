package cn.gyyx.action.beans.oa.lhzs.activityCode;

public class ActivityCodeChannelBean {
	private int code;
	private int actionCode;
	private String activityCode;
	private int channel;
	private String channelDes;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getActionCode() {
		return actionCode;
	}
	public void setActionCode(int actionCode) {
		this.actionCode = actionCode;
	}
	public String getActivityCode() {
		return activityCode;
	}
	public void setActivityCode(String activityCode) {
		this.activityCode = activityCode;
	}
	public int getChannel() {
		return channel;
	}
	public void setChannel(int channel) {
		this.channel = channel;
	}
	public String getChannelDes() {
		return channelDes;
	}
	public void setChannelDes(String channelDes) {
		this.channelDes = channelDes;
	}
	
}
