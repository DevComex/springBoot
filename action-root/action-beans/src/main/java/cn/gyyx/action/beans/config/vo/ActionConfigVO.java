package cn.gyyx.action.beans.config.vo;

public class ActionConfigVO {

	private Integer count;
	
	private Integer lotteryScore;

	private Integer exchangeScore;
	
	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

	public Integer getLotteryScore() {
		return lotteryScore;
	}

	public void setLotteryScore(Integer lotteryScore) {
		this.lotteryScore = lotteryScore;
	}

	public Integer getExchangeScore() {
		return exchangeScore;
	}

	public void setExchangeScore(Integer exchangeScore) {
		this.exchangeScore = exchangeScore;
	}
}
